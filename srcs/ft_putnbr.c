#include "libft.h"

void	ft_putnbr(int n)
{
	char	nb;

	nb = '0' + n;
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		write(1, &nb, 1);
}
